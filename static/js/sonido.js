const imagen1 = document.getElementById('imagenramo1');
const audio1 = document.getElementById('audioRamo1');
imagen1.addEventListener('mouseenter', () => {
  audio1.currentTime = 0;
  audio1.play();
});
imagen1.addEventListener('mouseleave', () => {
  audio1.pause();
  audio1.currentTime = 0;
});

